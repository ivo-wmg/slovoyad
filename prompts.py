# 3. ТЕГЛА ЗА ОЦЕНЯВАНЕ
# ═══════════════════════════════════════════════════════════════════════════

_DEFAULT_WEIGHTS: dict[str, float] = {
    "domain_specific": 0.35,
    "originality": 0.25,
    "trust": 0.20,
    "quality": 0.10,
    "significance": 0.10,
}


def _format_weights(weights: dict[str, float]) -> str:
    """Форматира теглата като текст за промпта."""
    labels = {
        "domain_specific": "Домейн-специфичен критерий",
        "originality": "Оригиналност",
        "trust": "Доверие и източници",
        "quality": "Качество и дълбочина",
        "significance": "Значимост и локалност",
    }
    lines = []
    for key, weight in weights.items():
        label = labels.get(key, key)
        lines.append(f"  • {label}: {int(weight * 100)}%")
    return "\n".join(lines)


