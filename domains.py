# ---------------------------------------------------------------------------
# Публична функция за достъп
# ---------------------------------------------------------------------------

def get_domain_config(domain: str) -> DomainConfig:
    """Връща ``DomainConfig`` за дадения домейн.

    Parameters
    ----------
    domain:
        Пълен домейн (напр. ``"news.bg"``). Търсенето е
        case-insensitive.

    Returns
    -------
    DomainConfig

    Raises
    ------
    UnsupportedDomainError
        Ако домейнът не е в регистъра.
    """
    key = domain.strip().lower()
    config = DOMAIN_REGISTRY.get(key)
    if config is None:
        raise UnsupportedDomainError(key)
    return config
