# ═══════════════════════════════════════════════════════════════════════════
# 3. ТЕГЛА ЗА ОЦЕНЯВАНЕ
# ═══════════════════════════════════════════════════════════════════════════

_DEFAULT_WEIGHTS: dict[str, float] = {
    "domain_specific": 0.35,
    "originality": 0.25,
    "trust": 0.20,
    "quality": 0.10,
    "significance": 0.10,
}


def _format_weights(weights: dict[str, float]) -> str:
    """Форматира теглата като текст за промпта."""
    labels = {
        "domain_specific": "Домейн-специфичен критерий",
        "originality": "Оригиналност",
        "trust": "Доверие и източници",
        "quality": "Качество и дълбочина",
        "significance": "Значимост и локалност",
    }
    lines = []
    for key, weight in weights.items():
        label = labels.get(key, key)
        lines.append(f"  • {label}: {int(weight * 100)}%")
    return "\n".join(lines)


# ═══════════════════════════════════════════════════════════════════════════
# 4. КОНСТРУКТОР НА ПРОМПТА
# ═══════════════════════════════════════════════════════════════════════════

def build_evaluation_prompt(
    article_data: dict,
    domain_config: DomainConfig,
) -> str:
    """Сглобява пълния промпт за Gemini оценка.

    Parameters
    ----------
    article_data:
        Речник с поне ``"title"`` и ``"text"`` ключове. Може да
        съдържа и ``"url"``, ``"author"``, ``"date"`` и др.
    domain_config:
        Инстанция на ``DomainConfig`` за съответния домейн.

    Returns
    -------
    str
        Финалният промпт, готов за изпращане към Gemini.
    """
    # Определяне на теглата – домейнът може да override-не глобалните
    weights = dict(_DEFAULT_WEIGHTS)
    if domain_config.weight_overrides:
        weights.update(domain_config.weight_overrides)

    # Извличане на полета от article_data
    title = article_data.get("title", "(без заглавие)")
    text = article_data.get("text", "")
    url = article_data.get("url", "")
    author = article_data.get("author", "")
    date = article_data.get("date", "")

    # Метаданни блок
    meta_parts = [f"Заглавие: {title}"]
    if url:
        meta_parts.append(f"URL: {url}")
    if author:
        meta_parts.append(f"Автор: {author}")
    if date:
        meta_parts.append(f"Дата: {date}")
    meta_block = "\n".join(meta_parts)

    # Сглобяване на промпта
    prompt = dedent(f"""\
        ╔══════════════════════════════════════════════════════════════╗
        ║          SLOVOYAD — ОЦЕНКА НА МЕДИЙНА ПУБЛИКАЦИЯ           ║
        ╚══════════════════════════════════════════════════════════════╝

        Домейн: {domain_config.name} ({domain_config.category})

        ──────────────────────────────────────────────────────────────
        ЧАСТ 1: УНИВЕРСАЛНИ КРИТЕРИИ
        ──────────────────────────────────────────────────────────────

        {UNIVERSAL_CRITERIA}

        ──────────────────────────────────────────────────────────────
        ЧАСТ 2: ДОМЕЙН-СПЕЦИФИЧНИ КРИТЕРИИ
        ──────────────────────────────────────────────────────────────

        {domain_config.specific_rules}

        ──────────────────────────────────────────────────────────────
        ТЕГЛА ЗА КРАЙНА ОЦЕНКА
        ──────────────────────────────────────────────────────────────

        Изчисли крайната претеглена оценка (weighted_score) по следните
        тегла:

        {_format_weights(weights)}

        Формулата е:
        weighted_score = сума от (оценка × тегло) за всяко измерение.

        ──────────────────────────────────────────────────────────────
        СТАТИЯ ЗА ОЦЕНЯВАНЕ
        ──────────────────────────────────────────────────────────────

        {meta_block}

        ── ТЕКСТ НА СТАТИЯТА ──────────────────────────────────────

        {text}

        ──────────────────────────────────────────────────────────────
        ФОРМАТ НА ОТГОВОРА
        ──────────────────────────────────────────────────────────────

        Отговори СТРОГО в следния JSON формат (без допълнителен текст
        извън JSON-а):

        {{
          "domain_specific_score": <int 1-10>,
          "domain_specific_reasoning": "<кратко обяснение>",
          "originality_score": <int 1-10>,
          "originality_reasoning": "<кратко обяснение>",
          "significance_score": <int 1-10>,
          "significance_reasoning": "<кратко обяснение>",
          "quality_score": <int 1-10>,
          "quality_reasoning": "<кратко обяснение>",
          "trust_score": <int 1-10>,
          "trust_reasoning": "<кратко обяснение>",
          "weighted_score": <float закръглен до 1 десетичен знак>,
          "summary": "<обобщение в 2-3 изречения>",
          "strengths": ["<силна страна 1>", "<силна страна 2>"],
          "weaknesses": ["<слаба страна 1>", "<слаба страна 2>"]
        }}
    """)

    return prompt
